    private void bttnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnUpdateActionPerformed
        // TODO add your handling code here:

        if (jTPersonagens.getSelectedRow() != -1) {

            Personagem p = new Personagem();
            PersonagemDAO dao = new PersonagemDAO();

            p.setNome(txtNome.getText());
            p.setSexo((String) cbSexo.getSelectedItem());
            p.setClasse((String) cbClasse.getSelectedItem());
            p.setId((int) jTPersonagens.getValueAt(jTPersonagens.getSelectedRow(), 0));
            dao.update(p);

            txtNome.setText("");
            cbClasse.setSelectedIndex(1);
            cbClasse.setSelectedIndex(1);

            readJTable();

        }


    }//GEN-LAST:event_bttnUpdateActionPerformed

