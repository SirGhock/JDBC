    private void bttnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnCreateActionPerformed
        // TODO add your handling code here:

        Personagem p = new Personagem(txtNome.getText(), (String) cbClasse.getSelectedItem(), (String) cbSexo.getSelectedItem());
        PersonagemDAO dao = new PersonagemDAO();

        dao.create(p);

        txtNome.setText("");
        cbClasse.setSelectedIndex(1);
        cbSexo.setSelectedIndex(1);

        readJTable();

    }//GEN-LAST:event_bttnCreateActionPerformed

    private void bttnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnDeleteActionPerformed
        // TODO add your handling code here:

        if (jTPersonagens.getSelectedRow() != -1) {

            Personagem p = new Personagem();
            PersonagemDAO dao = new PersonagemDAO();
            
            p.setId((int) jTPersonagens.getValueAt(jTPersonagens.getSelectedRow(), 0));
            
            dao.delete(p);

            txtNome.setText("");
            cbClasse.setSelectedIndex(1);
            cbClasse.setSelectedIndex(1);

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }


    }//GEN-LAST:event_bttnDeleteActionPerformed

    private void jTPersonagensMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTPersonagensMouseClicked
        // TODO add your handling code here:

        if (jTPersonagens.getSelectedRow() != -1) {

            txtNome.setText(jTPersonagens.getValueAt(jTPersonagens.getSelectedRow(), 1).toString());
            cbClasse.setSelectedItem(jTPersonagens.getValueAt(jTPersonagens.getSelectedRow(), 2).toString());
            cbSexo.setSelectedItem(jTPersonagens.getValueAt(jTPersonagens.getSelectedRow(), 4).toString());

        }

    }//GEN-LAST:event_jTPersonagensMouseClicked

