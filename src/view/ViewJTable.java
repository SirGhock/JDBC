    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTPersonagens.getModel();
        modelo.setNumRows(0);
        PersonagemDAO pdao = new PersonagemDAO();

        for (Personagem p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getClasse(),
                p.getLevel(),
                p.getSexo()
            });

        }

    }

